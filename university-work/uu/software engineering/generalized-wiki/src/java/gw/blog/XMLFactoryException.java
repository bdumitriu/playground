package gw.blog;

public class XMLFactoryException extends Exception {

    public XMLFactoryException() {
        super();
    }

    public XMLFactoryException(String message) {
        super(message);
    }

    public XMLFactoryException(String message, Throwable cause) {
        super(message, cause);
    }

    public XMLFactoryException(Throwable cause) {
        super(cause);
    }

}
